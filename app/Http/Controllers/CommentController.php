    {
        $request = $request->all();
        $request['created_at'] = Carbon::now();
        $request['user_id'] = Auth::user()->id;
        $result = Comment::create($request);

        if ($result)
        {
            return redirect()->action('PostController@show', $request['discussion_id']);
        }
    }

